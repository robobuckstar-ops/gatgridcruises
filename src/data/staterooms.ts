import type { Stateroom } from '@/types/database';

// Helper function to generate staterooms for a ship
function generateShipStaterooms(
  shipId: string,
  shipConfig: {
    name: string;
    minDeck: number;
    maxDeck: number;
    roomNumberPrefix: (deck: number, index: number) => string;
  },
  startRoomId: number
): Stateroom[] {
  const rooms: Stateroom[] = [];
  let roomId = startRoomId;
  const timestamp = '2026-01-01T00:00:00Z';

  const categoryConfigs = {
    inside: [
      { code: '11A', decks: [2, 3, 4], positions: ['mid', 'aft'], sides: ['interior'], maxOcc: 4 },
      { code: '11B', decks: [2, 3, 4], positions: ['forward', 'aft'], sides: ['interior'], maxOcc: 4 },
      { code: '10A', decks: [5, 6], positions: ['forward'], sides: ['interior'], maxOcc: 4 },
      { code: '10B', decks: [5, 6], positions: ['mid'], sides: ['interior'], maxOcc: 3 },
    ],
    oceanview: [
      { code: '9A', decks: [2, 3], positions: ['forward', 'aft'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '9B', decks: [4, 5], positions: ['mid'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '9C', decks: [6, 7], positions: ['mid'], sides: ['port', 'starboard'], maxOcc: 3 },
    ],
    verandah: [
      { code: '7A', decks: [6, 7], positions: ['forward', 'mid'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '6A', decks: [7, 8], positions: ['forward', 'aft', 'mid'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '5A', decks: [8, 9], positions: ['mid'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '5B', decks: [8, 9], positions: ['aft', 'mid'], sides: ['port', 'starboard'], maxOcc: 5 },
      { code: '5C', decks: [9, 10], positions: ['forward', 'mid'], sides: ['port', 'starboard'], maxOcc: 4 },
    ],
    concierge: [
      { code: '1A', decks: [11, 12], positions: ['mid', 'forward'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '1B', decks: [11, 12], positions: ['aft'], sides: ['port', 'starboard'], maxOcc: 4 },
      { code: '2A', decks: [12, 13], positions: ['forward'], sides: ['port', 'starboard'], maxOcc: 4 },
    ],
  };

  // Helper to pick values from arrays
  const pick = (array: any[], index: number) => array[index % array.length];
  const pickRand = (array: any[], seed: number) => array[Math.floor((seed * 9973) % array.length)];

  // Generate 8 inside staterooms
  for (let i = 0; i < 8; i++) {
    const catConfig = pick(categoryConfigs.inside, i);
    const deck = pick(catConfig.decks, i);
    const position = pick(catConfig.positions, i);
    const side = 'interior';
    const roomNum = shipConfig.roomNumberPrefix(deck, i);
    const isAccessible = i === 1; // One accessible room per ship
    const isConnecting = i % 5 === 0 && i > 0;

    rooms.push({
      id: `stateroom-${String(roomId).padStart(4, '0')}`,
      ship_id: shipId,
      room_number: roomNum,
      category: 'Inside',
      category_code: catConfig.code,
      deck,
      position,
      side,
      noise_rating: position === 'mid' && deck >= 5 ? 4 : position === 'forward' ? 2 : 2,
      view_rating: 1,
      connecting_room: isConnecting ? `stateroom-${String(roomId + 1).padStart(4, '0')}` : null,
      accessible: isAccessible,
      max_occupancy: catConfig.maxOcc,
      pros: isAccessible
        ? ['Wheelchair accessible', 'Larger layout for mobility', 'Central location']
        : position === 'mid'
          ? ['Quiet mid-ship location', 'Central to elevators', 'Value price point']
          : position === 'forward'
            ? ['Good elevator access', 'Budget-friendly', 'Forward position']
            : ['Budget-friendly', 'Peaceful location', 'Below waterline - cooler in hot climates'],
      cons: isAccessible
        ? ['Interior room', 'May feel confined']
        : position === 'forward'
          ? ['Interior - no views', 'Forward can pitch slightly']
          : ['Interior - no windows', 'No natural light'],
      notes: isAccessible ? 'Accessible stateroom with grab bars and modified layout' : null,
      created_at: timestamp,
      updated_at: timestamp,
    });
    roomId++;
  }

  // Generate 5 oceanview staterooms
  for (let i = 0; i < 5; i++) {
    const catConfig = pick(categoryConfigs.oceanview, i);
    const deck = pick(catConfig.decks, i);
    const position = pick(catConfig.positions, i);
    const side = i % 2 === 0 ? 'port' : 'starboard';
    const roomNum = shipConfig.roomNumberPrefix(deck, 10 + i);

    rooms.push({
      id: `stateroom-${String(roomId).padStart(4, '0')}`,
      ship_id: shipId,
      room_number: roomNum,
      category: 'Oceanview',
      category_code: catConfig.code,
      deck,
      position,
      side,
      noise_rating: position === 'mid' && deck >= 5 ? 4 : deck <= 3 ? 2 : 3,
      view_rating: 3,
      connecting_room: null,
      accessible: false,
      max_occupancy: catConfig.maxOcc,
      pros: [
        'Natural light and fresh air',
        'Ocean views',
        position === 'mid' ? 'Quiet mid-ship location' : position === 'forward' ? 'Forward views' : 'Aft sunset views',
      ],
      cons: [
        position === 'forward' ? 'Some forward motion in waves' : 'Standard cabin dimensions',
        'Mid-range pricing',
      ],
      notes: null,
      created_at: timestamp,
      updated_at: timestamp,
    });
    roomId++;
  }

  // Generate 12 verandah staterooms
  for (let i = 0; i < 12; i++) {
    const catConfig = pick(categoryConfigs.verandah, i);
    const deck = pick(catConfig.decks, i);
    const position = pick(catConfig.positions, i);
    const side = i % 2 === 0 ? 'port' : 'starboard';
    const roomNum = shipConfig.roomNumberPrefix(deck, 20 + i);

    rooms.push({
      id: `stateroom-${String(roomId).padStart(4, '0')}`,
      ship_id: shipId,
      room_number: roomNum,
      category: 'Verandah',
      category_code: catConfig.code,
      deck,
      position,
      side,
      noise_rating: position === 'mid' && deck >= 8 ? 4 : position === 'forward' ? 2 : 2,
      view_rating: deck >= 9 ? 5 : 4,
      connecting_room: null,
      accessible: false,
      max_occupancy: catConfig.maxOcc,
      pros: [
        'Private verandah',
        position === 'mid' ? 'Quiet mid-ship location' : position === 'forward' ? 'Great forward views' : 'Sunset watching',
        'Spacious cabin',
      ],
      cons: [
        position === 'forward' ? 'Forward position feels some ship motion' : 'Verandah category pricing',
        'Premium pricing',
      ],
      notes: null,
      created_at: timestamp,
      updated_at: timestamp,
    });
    roomId++;
  }

  // Generate 5 concierge staterooms
  for (let i = 0; i < 5; i++) {
    const catConfig = pick(categoryConfigs.concierge, i);
    const deck = Math.min(pick(catConfig.decks, i), shipConfig.maxDeck);
    const position = pick(catConfig.positions, i);
    const side = i % 2 === 0 ? 'port' : 'starboard';
    const roomNum = shipConfig.roomNumberPrefix(deck, 30 + i);
    const isAccessible = i === 0;

    rooms.push({
      id: `stateroom-${String(roomId).padStart(4, '0')}`,
      ship_id: shipId,
      room_number: roomNum,
      category: 'Concierge',
      category_code: catConfig.code,
      deck,
      position,
      side,
      noise_rating: deck >= 11 ? 4 : 5,
      view_rating: 5,
      connecting_room: null,
      accessible: isAccessible,
      max_occupancy: catConfig.maxOcc,
      pros: [
        'Concierge lounge access',
        `Exclusive deck ${deck} location`,
        'Exceptional ocean views',
        'Priority dining reservations',
        isAccessible ? 'Wheelchair accessible' : 'Premium verandah',
      ],
      cons: ['Highest price category', 'Limited availability'],
      notes: isAccessible ? 'Premium concierge stateroom with accessible features and exclusive amenities' : null,
      created_at: timestamp,
      updated_at: timestamp,
    });
    roomId++;
  }

  return rooms;
}

